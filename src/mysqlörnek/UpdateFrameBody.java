    private void Btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_UpdateActionPerformed
       LoginPage lp = new LoginPage();
        if (txt_ID.getText() != null) {

            Connection con = getConnection();
            try {
                PreparedStatement ps = con.prepareStatement("UPDATE body_measurements SET Weight=?,Length=?,Biceps=?,Breast=?,Waists=? , "
                        + "Calf_and_Leg=?,Neck=?,Shoulder=?,Hip=?,Wrist=?,Oil_Percentage=?,Muscle_Weight=?  WHERE ID=?");

                //PS.SET... KISMINAN SEÇEREK GÖNDERİYORUZ
                ps.setString(1, txt_Bicepsup.getText());

                ps.setString(2, txt_Weightup.getText());

                ps.setString(3, txt_Lengthup.getText());

                ps.setString(4, txt_Breastup.getText());

                ps.setString(5, txt_Calf_and_Legup.getText());

                ps.setString(6, txt_Hipup.getText());

                ps.setString(7, txt_Neckup.getText());

                ps.setString(8, txt_Shoulderup.getText());

                ps.setString(9, txt_Waistsup.getText());

                ps.setString(10, txt_Wristup.getText());

                ps.setString(11, txt_Oil_Percetageup.getText());

                ps.setString(12, txt_Muscle_Weightup.getText());

                ps.setString(13, txt_ID.getText());

                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Update");

                setVisible(false);
                lp.setVisible(true);

            } catch (SQLException ex) {
                Logger.getLogger(UpdateFrameBody.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }

        }
    }//GEN-LAST:event_Btn_UpdateActionPerformed

