    private void Btn_ShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ShowActionPerformed

        Connection con = getConnection();

        try {
            Object ID = txt_ID.getText();
            String querybody = "SELECT * FROM body_measurements Where ID=" + ID;

            Statement st2 = con.createStatement();
            ResultSet rs2 = st2.executeQuery(querybody);

            while (rs2.next()) {
                txt_Weight.setText(rs2.getString("Weight"));

                txt_Length.setText(rs2.getString("Length"));

                txt_Biceps.setText(rs2.getString("Weight"));

                txt_Breast.setText(rs2.getString("Breast"));

                txt_Waists.setText(rs2.getString("Waists"));

                txt_Calf_and_Leg.setText(rs2.getString("Calf_and_Leg"));

                txt_Neck.setText(rs2.getString("Neck"));

                txt_Shoulder.setText(rs2.getString("Shoulder"));

                txt_Hip.setText(rs2.getString("Hip"));

                txt_Wrist.setText(rs2.getString("Wrist"));

                txt_Oil_Percetage.setText(rs2.getString("Oil_Percentage"));

                txt_Muscle_Weight.setText(rs2.getString("Muscle_Weight"));

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_Btn_ShowActionPerformed

